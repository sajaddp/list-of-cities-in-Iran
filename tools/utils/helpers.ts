export const sortAllInterfaceArray = (data: AllInterface[]): AllInterface[] => {
  const typeOrder: Record<AllInterface["type"], number> = {
    province: 1,
    county: 2,
    city: 3,
    district: 4,
    rural: 5,
  };

  return data.sort((a, b) => typeOrder[a.type] - typeOrder[b.type]);
};
